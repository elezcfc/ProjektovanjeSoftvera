    private void btnUpdateDrinkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateDrinkActionPerformed

        try {
            int index = jTblDrinks.getSelectedRow();
            Pice p = (Pice) dtm.getListOfDrinks().get(index);
            Pice p1 = Controller.getControllerInstance().createDrink(p);
            if(p1 != null){
                listOfDrinks.remove(p);
                listOfDrinks.add(p1);
                dtm.setListOfDrinks(listOfDrinks);
                dtm.fireTableDataChanged();
                JOptionPane.showMessageDialog(this, "Sistem je zapamtio izmene.");
            }
        } catch (Exception ex) {
            Logger.getLogger(DrinksFrm.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnUpdateDrinkActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String filterWord = jtfSearch.getText();
        List<AbstractObject> filteredList;
        if(filterWord.equals("")){
            dtm.setListOfDrinks(listOfDrinks);
            dtm.fireTableDataChanged();
        }else{
            try {
                filteredList = Controller.getControllerInstance().searchDrinks(filterWord);
                if(filteredList.isEmpty()){
                    JOptionPane.showMessageDialog(this, "Sistem nije nasao pice po zadatim vrednostima.");
                }else{
                    JOptionPane.showMessageDialog(this, "Sistem je nasao pice po zadatim vrednostima.");
                    dtm.setListOfDrinks(filteredList);
                    dtm.fireTableDataChanged();
                }
            } catch (Exception ex) {
                Logger.getLogger(DrinksFrm.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnSearchActionPerformed

